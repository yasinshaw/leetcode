# @lc code=start
class Solution:
    def coinChange(self, coins: List[int], amount: int) -> int:
        # 动态规划
        dp = [sys.maxsize] * (amount + 1)
        dp[0] = 0
        for coin in coins:
            for j in range(coin, amount + 1):
                dp[j] = min(dp[j], dp[j - coin] + 1)

        return dp[-1] if dp[-1] < sys.maxsize else -1

